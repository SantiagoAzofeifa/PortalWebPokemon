package cr.ac.una.portalwebpokeapi.model;

/**
 * Enumeración de categorías disponibles para productos o elementos del catálogo.
 * Se utiliza para clasificar productos, ítems o generaciones dentro del sistema.
 */
public enum Category {
    /** Pokémon individuales u otros elementos biológicos. */
    POKEMON,

    /** Especies o variantes de Pokémon. */
    SPECIES,

    /** Objetos, ítems o equipamiento del juego. */
    ITEMS,

    /** Generaciones o versiones de los juegos de Pokémon. */
    GENERATIONS
}
